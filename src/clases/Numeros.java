package clases;

public class Numeros {
    
    public static double suma(Object a, Object b){
        return (double)a + (double)b;
    }        
    
    public static double resta(double a, double b){
        return a - b;
    }
    
    public static double multiplica(double a, double b){
        return a * b;
    }
    
    

}
